        function sendEmail() {
            var name = $("#name");
            var email = $("#email");
            var address = $("#address");
            var phone = $("#phone");
            var subject = $("#subject");
            var remarks = $("#remarks");

            if(isNotEmpty(name) && isNotEmpty(email) && isNotEmpty(subject) && isNotEmpty(remarks)){
                $.ajax({
                    url: "sendEmail.php",
                    method: "POST", 
                    dataType: "json",
                    data: {
                        name: name.val(),
                        email: email.val(),
                        address: address.val(),
                        phone: phone.val(),
                        subject: subject.val(),
                        remarks: remarks.val()
                    }, success: function(response) {
                        $('#myForm')[0].reset();
                        $('.sent-notification').text("Message sent successfully.");
                    }
                });
            }
        }
        function isNotEmpty(caller){
            if(caller.val()==""){
                caller.css('border', '1px solid red');
                return false;
            }
            else{
                caller.css('border','');
                return true;
            }
        }
